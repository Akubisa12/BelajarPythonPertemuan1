"""
Pengertian
    Operator assignment (penugasan) adalah operator yang digunakan untuk memberikan nilai ke sebuah variabel. Operator assignment paling dasar adalah tanda sama dengan (=).
"""

# 1. Addition Asignment (+=)
# Menambahkan nilai ke variabel dan menyimpan hasilnya.
x = 5
# 5 += 3  equivalen 5 += 3
x += 3  # sama dengan: x = x + 3
print(x)  # Output: 8

# 2. Subtraction Assignment (-=)
# Mengurangi nilai variabel dan menyimpan hasilnya.
# x = 10
# x -= 4  # sama dengan: x = x - 4
# print(x)  # Output: 6


# 3. Multiplication Assignment (*=)
# Mengalikan nilai variabel dan menyimpan hasilnya.
# x = 5
# x *= 2  # sama dengan: x = x * 2
# print(x)  # Output: 10

# 4. Division Assignment (/=)
# Membagi nilai variabel dan menyimpan hasilnya.
# x = 20
# x /= 4  # sama dengan: x = x / 4
# print(x)  # Output: 5.0


# 5. Floor Division Assignment (//=)
# Membagi nilai variabel dengan pembagian bulat ke bawah.
# x = 5
# // = pembagian tetapi dia hasilnya dibulatkan ke bawah
# / = pembagian biasa hasilnya ga ada pembulatan
# x //= 2  # sama dengan: x = x // 5
# print(x)  # Output: 3

# 6. Modulus(Sisa bagi) Assignment (%=)
# Mengambil sisa pembagian dan menyimpannya.
# x = 17
# x %= 5  # sama dengan: x = x % 5
# print(x)  # Output: 2

# 7. Exponentiation Assignment (=)**
# Memangkatkan nilai variabel dan menyimpan hasilnya.
# x = 2
# x **= 3  # sama dengan: x = x ** 3
# print(x)  # Output: 8

